//	 dt = Change in time (in seconds) since the last game loop.
extern float randomfloatrange(float rangeMin, float rangeMax)
{
//return ((float)rand() / rangeMin ) + rangeMax;
float random = ((float)rand()) / (float)RAND_MAX;
float diff = rangeMax - rangeMin;
float r = random * diff;
return rangeMin + r;
}

